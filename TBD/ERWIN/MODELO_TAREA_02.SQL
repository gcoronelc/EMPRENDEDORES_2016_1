CREATE TABLE PRODUCTO
( 
	CODPRODUCTO          integer  NOT NULL ,
	NOMPRODUCTO          varchar(30)  NULL ,
	UNIDAD               varchar(15)  NULL ,
	PRECIO               numeric(10,2)  NULL ,
	CODCATEGORIA         char(3)  NOT NULL ,
	CODPROVEEDOR         char(3)  NOT NULL 
)
go



ALTER TABLE PRODUCTO
	ADD CONSTRAINT XPKPRODUCTO PRIMARY KEY (CODPRODUCTO ASC)
go



