    private void mostrarDatos(List<Profesor> lista) {
    // Acceso al TableModel
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tablaDatos.getModel();
    // Limpiar la tabla
    tabla.setRowCount(0);
    // Cargar lista en tabla
    for (Profesor p : lista) {
      Object[] rowData = {p.getCodigo(), p.getApellido(),
        p.getNombre(), p.getDireccion(),p.getEmail()};
      tabla.addRow(rowData);
    }
  }

