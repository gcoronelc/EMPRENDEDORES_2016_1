  private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
    try {
      // Datos
      Profesor bean = new Profesor();
      bean.setCodigo(txtCodigo.getText());
      bean.setApellido(txtApellido.getText());
      bean.setNombre(txtNombre.getText());
      bean.setEmail(txtEmail.getText());
      bean.setVacio();
      // Proceso
      EdutecService service = new EdutecService();
      List<Profesor> lista = service.conProfesores(bean);
      // Reporte
      mostrarDatos(lista);
    } catch (Exception e) {
      JOptionPane.showMessageDialog(rootPane, e.getMessage());
    }
  }//GEN-LAST:event_btnConsultarActionPerformed


