  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    // Datos
    BecaDto dto = new BecaDto();
    dto.setCategoria(cboCategoria.getSelectedItem().toString());
    dto.setProducto(cboProducto.getSelectedItem().toString());
    dto.setMeses(Integer.parseInt(cboMeses.getSelectedItem().toString()));
    // Proceso
    BecaService service = new BecaService();
    service.procesar(dto);
    // Reporte
    String repo =  "";
    repo += "Valor Cuota: " + dto.getValorCuota() + "\n";
    repo += "Porcentaje: " + dto.getPorcentaje() + "%\n";
    repo += "Importe: " + dto.getImporte()+ "\n";
    JOptionPane.showMessageDialog(rootPane, repo);
            
  }//GEN-LAST:event_btnProcesarActionPerformed

