  private void initCombos() {
    estoyOcupado = true;
    cboCategoria.setSelectedIndex(-1);
    cboProducto.setSelectedIndex(-1);
    cboMeses.setSelectedIndex(-1);
    lblImporte.setText("0.00");
    estoyOcupado = false;
  }
  
  private void cargarImporte(){
    // Validar
    int idxCategoria = cboCategoria.getSelectedIndex();
    int idxProducto = cboProducto.getSelectedIndex();
    if(idxCategoria == -1 || idxProducto == -1){
      lblImporte.setText("0.00");
      return;
    }
    // Valores correctos
    String txtCategoria = cboCategoria.getSelectedItem().toString();
    String txtProducto = cboProducto.getSelectedItem().toString();
    // Obtener el importe
    BecaService service = new BecaService();
    double importe = service.obtenerImporte(txtCategoria, txtProducto);
    lblImporte.setText(String.valueOf(importe));
  }
  
